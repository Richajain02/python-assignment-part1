subjects     = ["Math", "Physics", "CS", "English", "Chemistry"]
marks        = [88, 72, 95, 60, 78]
#Using a for loop to print each subject alongside its marks and a grade label.
for i in range(len(subjects)):
    score = marks[i]
    if score>=90:
      grade = "A+"
    elif score>=80:
      grade = "A"
    elif score>=70:
      grade = "B"
    elif score>=60:
      grade = "C"
    else:
      grade = "F"
    print(f"{subjects[i]}: {score} ({grade})")

#Calculate and print: Total marks (sum of all subjects)
                    # Average marks (rounded to 2 decimal places)
                    # Highest scoring subject (name + marks)
                    # Lowest scoring subject (name + marks)
total_marks = sum(marks)
avg_marks = round(total_marks/len(marks),2)
highest_score = max(marks)
lowest_score = min(marks)
print(f"\nTotal Marks: {total_marks}")
print(f"Average Marks: {avg_marks}")
print(f"Highest Scoring Subject: {subjects[marks.index(highest_score)]} ({highest_score})")
print(f"Lowest Scoring Subject: {subjects[marks.index(lowest_score)]} ({lowest_score})")


#While loop for marks-entry system
print("\n--- Marks Entry System (Type 'done' as subject name to stop)---")
new_subjects_count = 0
while True:
    sub_input = input("Enter subject name: ").strip()
    if sub_input.lower() == 'done':
        break
    mark_input = input(f"Enter marks for {sub_input}: ")
    if not mark_input.replace('.','',1).isdigit():
        print("Warning: Non-Numeric input detected. Please enter a numeric value between 0-100.")
        continue

    val = float(mark_input)
    if val < 0 or val > 100:
        print(f"Warning: {val} is outside the range 0-100. Entry ignored.")
        continue
    subjects.append(sub_input)
    marks.append(val)
    new_subjects_count += 1
updated_avg = round(sum(marks) / len(marks), 2)
print(f"\nNew subjects added: {new_subjects_count}")
print(f"Updated average across all {len(subjects)} subjects: {updated_avg}")

#Task 3
class_data = [
    ("Ayesha Sharma",  [88, 72, 95, 60, 78]),
    ("Rohit Verma",    [55, 68, 49, 72, 61]),
    ("Priya Nair",     [91, 85, 88, 94, 79]),
    ("Karan Mehta",    [40, 55, 38, 62, 50]),
    ("Sneha Pillai",   [75, 80, 70, 68, 85]),
]
#Variables to store summary data
pass_count = 0
fail_count = 0
all_averages = []
class_topper_name = ""
highest_avg = -1

#Print the Header and Loop
print(f"{'Name':<18} | {'Average':<8} | {'Status'}")
print("-" * 45) #Separating the labels from the data.

for name, marks in class_data:
    avg = round(sum(marks) / len(marks), 2)  # Compute average
    all_averages.append(avg)
    status = "Pass" if avg >= 60 else "Fail" # Assign status

    # Update pass/fail counters
    if status == "Pass":
        pass_count += 1
    else:
        fail_count += 1

    # Check for topper
    if avg > highest_avg:
        highest_avg = avg
        class_topper_name = name

    print(f"{name:<18} | {avg:<8.2f} | {status}")
print("-" * 45) #Print Final Summary
class_avg = round(sum(all_averages) / len(all_averages), 2)

print(f"Number of students who passed: {pass_count}")
print(f"Number of students who failed: {fail_count}")
print(f"Class topper: {class_topper_name} ({highest_avg})")
print(f"Class average: {class_avg}")

