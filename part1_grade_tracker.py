#Task 1
raw_students = [
    {"name": "  ayesha SHARMA  ", "roll": "101", "marks_str": "88, 72, 95, 60, 78"},
    {"name": "ROHIT verma", "roll": "102", "marks_str": "55, 68, 49, 72, 61"},
    {"name": "  Priya Nair  ", "roll": "103", "marks_str": "91, 85, 88, 94, 79"},
    {"name": "karan MEHTA", "roll": "104", "marks_str": "40, 55, 38, 62, 50"},
    {"name": " Sneha pillai ", "roll": "105", "marks_str": "75, 80, 70, 68, 85"}
]
cleaned_students = []

# Process raw_students to create cleaned_students
for student in raw_students:
    name = student["name"].strip().title() # Strip whitespace and title case:
    roll = int(student["roll"])  # Convert string to integer:

    # Split string and convert each string to integer:
    marks = list(map(int, student["marks_str"].split(",")))

    # Create the cleaned dictionary:
    cleaned_data = {
        "name": name,
        "roll": roll,
        "marks": marks
    }
    cleaned_students.append(cleaned_data)

# For each cleaned student, verify the name is valid: check that every word in the name contains only alphabetic characters. Print "✓ Valid name" or "✗ Invalid name" next to each student.
for student_data in cleaned_students:
    name = student_data["name"]
    words = name.split()
    is_valid_name = True
    for word in words:
        if not word.isalpha():
            is_valid_name = False
            break
    validation_text= "✓ Valid name" if is_valid_name else "✗ Invalid name"

    # Print a formatted profile card for each cleaned student using f-strings:
    print("================================")
    print(f"Student : {student_data['name']} ({validation_text})")
    print(f"Roll No: {student_data['roll']}")
    print(f"Marks: {student_data['marks']}")
    print("================================")
    print() #Space for next student

# After processing all students, print the name in ALL CAPS and lowercase for the student with roll number 103.
for student_data in cleaned_students:
    if student_data["roll"] == 103:
        print(f"Roll 103 Upper: {student_data['name'].upper()}")
        print(f"Roll 103 Lower: {student_data['name'].lower()}")
        break

